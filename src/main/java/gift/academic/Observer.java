package gift.academic;

/**
 * Observer (Наблюдатель) — это "слушатель" событий.
 * Он подписывается на Observable и реагирует на всё, что в него прилетает.
 *
 * Жизненный цикл вызовов обычно такой:
 * onSubscribe -> onNext (много раз) -> onComplete (или onError).
 *
 * @param <T> Тип данных, который этот наблюдатель ожидает получить в конце цепочки.
 */
public interface Observer<T> {

    /**
     * onNext (Получение данных)
     *
     * Основной рабочий метод. Сюда "прилетают" уже очищенные, отфильтрованные
     * и преобразованные данные. Если в цепочке был .map(), то здесь вы
     * получите уже конечный результат трансформации.
     *
     * @param item Элемент данных (объект типа T).
     */
    void onNext(T item);


    /**
     * onError (Обработка сбоя)
     *
     * Метод-спасатель. Если на любом этапе (в источнике, в filter или в map)
     * произойдет исключение (Exception), оно не "уронит" приложение, а прилетит сюда.
     *
     * ВАЖНО: После этого вызова цепочка разрывается, и onNext больше не вызовется.
     *
     * @param t Объект ошибки (исключение).
     */
    void onError(Throwable t);


    /**
     * onComplete (Успешный финал)
     *
     * Сигнал о том, что всё прошло успешно и данных больше не будет.
     * Здесь обычно скрывают индикаторы загрузки (ProgressBar) или
     * выводят сообщение "Загрузка завершена".
     */
    void onComplete();

    /**
     * onSubscribe (Подписка оформлена)
     *
     * Метод, который вызывается в момент вызова .subscribe().
     * Он дает нам объект Disposable, через который мы можем "перерезать провод",
     * то есть отписаться от обновлений в любой момент (например, если пользователь
     * закрыл экран, не дождавшись загрузки).
     *
     * @param d Объект управления подпиской.
     */
    void onSubscribe(Disposable d);
}
