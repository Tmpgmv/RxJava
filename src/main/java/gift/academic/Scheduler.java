package gift.academic;

/**
 * Scheduler (Планировщик) — это абстракция над механизмом выполнения задач.
 *
 * В реактивном программировании мы не создаем потоки (Thread) вручную.
 * Вместо этого мы просим "Планировщик" выполнить задачу, а он сам решает:
 * создать ли новый поток, взять ли поток из пула или выполнить задачу в текущем потоке.
 */
@FunctionalInterface
public interface Scheduler {
    /**
     * execute (Выполнить задачу)
     *
     * Основной и единственный метод интерфейса.
     * Он принимает "задачу" (Runnable) и отправляет её на выполнение.
     *
     * Именно этот метод вызывается внутри операторов:
     * 1. subscribeOn — отправляет в планировщик процесс подписки.
     * 2. observeOn — отправляет в планировщик доставку данных (onNext).
     *
     * @param task Объект Runnable, содержащий код, который нужно запустить
     *             в контексте данного планировщика.
     */
    void execute(Runnable task);
}